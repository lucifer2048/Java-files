public class student3 {
        int rollno;
        String name;
        public student3(String name, int rollno) {
            this.name = name;
            this.rollno = rollno;
        }
    
        public String getName() {
            return name;
        }
    
        public void setName(String name) {
            this.name = name;
        }
    
        public int getRollno() {
            return rollno;
        }
    
        public void setRollno(int rollno) {
            this.rollno = rollno;
        }
    
}
